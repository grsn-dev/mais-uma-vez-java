package javaAgoraVai;

public class Concatenar {

	public static void main(String[] args) {

		double x = 10.35784;
		String nome = "Maria";
		int idade = 32;
		double renda = 4000.0;

		System.out.printf("Mensagem para concatenar: %.2f metros%n", x);
		System.out.printf("%s tem %d anos e ganha %.2f reais%n", nome, idade, renda);

	}

}